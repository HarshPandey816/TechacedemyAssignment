package AssignmentDay6;

class ValueOfDemo {
	public static void main(String[] args) {

        int num = 100;
        String str = String.valueOf(num);

        double d = 10.5;
        String str2 = String.valueOf(d);

        System.out.println("String from int: " + str);
        System.out.println("String from double: " + str2);
    }
}
